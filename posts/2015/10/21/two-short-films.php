		<h1>Two short films about household food waste in Philippines and India</h1>
		<p>21 October 2015</p> <!-- POST DATE-->
	</div>	
	<div class="postcontent">
		<p><a href="http://www.snis.ch/" title="The Swiss network for International Studies">The Swiss Network for International Studies</a> has been doing work on food consumption in various regions around the world under the Food Consumption project. They've recently released two short films about <a href="http://foodconsumption.snis.ch/eating-in-and-food-waste-in-bangalore/" title="Eating in and food waste in Bangalore | Food consumption">Eating In and Food Waste in Bangalore</a> and <a href="http://foodconsumption.snis.ch/eating-out-and-food-waste-in-metro-manila/" title="Eating out and food waste in Metro Manila | Food consumption">Eating Out and Food Waste in Metro Manila</a>.</p> 

		<p>While the videos are quite short, and offer only quick glimpses into people's food prep and consumption tendencies, I thoroughly enjoyed these videos for a number of reasons. The broader cultural differences in shopping, preparation, and domestic roles is rather compelling, and is important to remember when we think about our own tendencies.</p>

		<p>I was also interested in some of the recent shifts that have taken place to change food dynamics, such as the increased tendency for both parents to work outside of the house, the availability of domestic help, and the growing prevalence of supermarkets.</p>

		<p>Finally, I appreciated the little glimpses of household cooking areas. I'm always fascinated by people's kitchens, the differences and similarities between them, and what they can accomplish in those spaces.</p>

		<p>While these are just bitesized little videos, I encourage you to take a peek.</p>
		
